// delete the  first element  form an array (or) shift() method : 
//------------------------------------------------------------------
let ninjaMovies = ["Stealth", "Strike", "Shadow Clone", "Escape"]
let removeNinja = ninjaMovies.shift()
console.log(removeNinja) // Stealth
console.log(ninjaMovies)  // [ 'Strike', 'Shadow Clone', 'Escape' ]